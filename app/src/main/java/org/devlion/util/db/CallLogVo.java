package org.devlion.util.db;

public class CallLogVo {

    private String cellPhone;
    private String companyPhone;
    private String companyCode;
    private String addisplayName;
    private String chargeJob;
    private String mainDeptCode;
    private String companyName;
    private String deptName;

    public String getCellPhone() {
        return cellPhone;
    }

    public void setCellPhone(String cellPhone) {
        this.cellPhone = cellPhone;
    }

    public String getCompanyPhone() {
        return companyPhone;
    }

    public void setCompanyPhone(String companyPhone) {
        this.companyPhone = companyPhone;
    }

    public String getCompanyCode() {
        return companyCode;
    }

    public void setCompanyCode(String companyCode) {
        this.companyCode = companyCode;
    }

    public String getAddisplayName() {
        return addisplayName;
    }

    public void setAddisplayName(String addisplayName) {
        this.addisplayName = addisplayName;
    }

    public String getChargeJob() {
        return chargeJob;
    }

    public void setChargeJob(String chargeJob) {
        this.chargeJob = chargeJob;
    }

    public String getMainDeptCode() {
        return mainDeptCode;
    }

    public void setMainDeptCode(String mainDeptCode) {
        this.mainDeptCode = mainDeptCode;
    }

    public String getCompanyName() {
        return companyName;
    }

    public void setCompanyName(String companyName) {
        this.companyName = companyName;
    }

    public String getDeptName() {
        return deptName;
    }

    public void setDeptName(String deptName) {
        this.deptName = deptName;
    }
}
